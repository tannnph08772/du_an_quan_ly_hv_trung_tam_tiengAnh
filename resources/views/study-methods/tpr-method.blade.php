                    <p><strong>TPR ( Total Physical Response – Tổng Phản Ứng Vật Lý) </strong></p>
                    <p>Phương pháp này được sáng tạo bởi giáo sư tâm lý học James Asher, giảng viên trường Đại học bang
                        San José, này có mục đích giúp học viên phát triển ngôn ngữ thứ hai một cách tự nhiên. Nó được
                        biết đến và áp dụng rộng rãi trên toàn cầu như là một cách dạy Tiếng Anh có hiệu quả truyền đạt
                        cao cũng như mang lại nhiều niềm vui cho mọi người. Thành công của TPR là nhờ vào việc phối hợp
                        giữa việc học một ngôn ngữ mới gắn với các hoạt động thể chất để kích thích sự ghi nhớ của não
                        bộ, giúp học viên cảm thấy hứng thú và vui vẻ khi học tiếng Anh, coi nó là sở thích thú vị, hữu
                        ích thay vì cảm giác bị ép buộc phải học.</p>

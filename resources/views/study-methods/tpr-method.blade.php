                    <p>Asher đã phát triển TPR như là kết quả của kinh nghiệm của mình khi quan sát trẻ nhỏ học ngôn ngữ
                        đầu tiên của chúng. Ông nhận thấy rằng sự tương tác giữa cha mẹ và trẻ em thường mang hình thức
                        phát biểu từ cha mẹ theo sau là một phản ứng vật lý từ đứa trẻ. Asher đã đưa ra ba giả thuyết
                        dựa trên những quan sát của mình: thứ nhất, ngôn ngữ đó được học chủ yếu bằng cách lắng nghe;
                        thứ hai, việc học ngôn ngữ đó phải tham gia bán cầu não phải; và thứ ba, ngôn ngữ học tập đó
                        không liên quan đến bất kỳ căng thẳng nào.</p>
